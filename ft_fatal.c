#include "printf.h"

void	ft_fatal(char *binary)
{
	if (!binary)
		return ;
	ft_printf_fd(2, "\033[1;31m[usage:]\033[0m %s <arg1> <arg2> <...>\n"\
			, binary);
	exit(EXIT_SUCCESS);
}
